local file = require 'dynamic-theme.file'

--- @class DynamicThemeExport
--- @field export_colorscheme fun(colorscheme_index: number): nil
local M = {}

--- @param colorscheme_index number
--- @return nil
M.export_colorscheme = function(colorscheme_index)
  local theme_list = file.read()
  if
    not theme_list
    or colorscheme_index < 1
    or colorscheme_index > #theme_list
  then
    vim.notify('Invalid theme index', vim.log.levels.ERROR)
    return
  end

  local theme = theme_list[colorscheme_index]
  if theme.name == '<EMPTY>' then
    vim.notify('Cannot export an empty theme', vim.log.levels.ERROR)
    return
  end

  if not theme.palette then
    vim.notify('Theme has no palette to export', vim.log.levels.ERROR)
    return
  end

  -- prompt user for export location
  local export_path = vim.fn.input {
    prompt = 'Export location: ',
    default = vim.fn.expand '~/',
    completion = 'dir',
  }

  if export_path == '' then
    vim.notify('Export cancelled', vim.log.levels.INFO)
    return
  end

  -- ensure path ends with /
  if export_path:sub(-1) ~= '/' then
    export_path = export_path .. '/'
  end

  -- create theme plugin name (normalize for filesystem)
  local plugin_name = theme.name:gsub(' ', '-'):lower() .. '.nvim'
  local plugin_path = export_path .. plugin_name

  -- create directory structure
  local dirs = {
    plugin_path,
    plugin_path .. '/lua',
    plugin_path .. '/lua/' .. plugin_name:gsub('%.nvim$', ''),
    plugin_path .. '/colors',
  }

  for _, dir in ipairs(dirs) do
    local success = vim.fn.mkdir(dir, 'p')
    if success == 0 then
      vim.notify('Failed to create directory: ' .. dir, vim.log.levels.ERROR)
      return
    end
  end

  -- create plugin files
  local files = {
    {
      path = plugin_path
        .. '/lua/'
        .. plugin_name:gsub('%.nvim$', '')
        .. '/init.lua',
      content = M.generate_init_file(theme),
    },
    {
      path = plugin_path
        .. '/lua/'
        .. plugin_name:gsub('%.nvim$', '')
        .. '/palette.lua',
      content = M.generate_palette_file(theme),
    },
    {
      path = plugin_path
        .. '/colors/'
        .. theme.name:gsub(' ', '-'):lower()
        .. '.lua',
      content = M.generate_colors_file(theme),
    },
    {
      path = plugin_path .. '/README.md',
      content = M.generate_readme(theme),
    },
  }

  for _, file_info in ipairs(files) do
    local file_handle = io.open(file_info.path, 'w')
    if file_handle then
      file_handle:write(file_info.content)
      file_handle:close()
    else
      vim.notify(
        'Failed to write file: ' .. file_info.path,
        vim.log.levels.ERROR
      )
    end
  end

  vim.notify('Theme exported to: ' .. plugin_path, vim.log.levels.INFO)
end

--- @param theme Colorscheme
--- @return string content
M.generate_init_file = function(theme)
  return string.format(
    [[
-- %s: A Neovim colorscheme
-- Generated by [dynamic-theme.nvim](https://github.com/alistairjoelquinn/dynamic-theme.nvim)

local M = {}

M.setup = function()
  local palette = require '%s.palette'
  
  -- Create highlight groups based on the palette
  local highlight_groups = M.create_highlight_groups(palette)
  
  -- Apply highlight groups
  for group, settings in pairs(highlight_groups) do
    vim.api.nvim_set_hl(0, group, settings)
  end
end

